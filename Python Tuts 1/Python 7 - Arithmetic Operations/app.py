#arithmetic operation in Python

#multiplication
print(12 * 8)

#addition
print(12 + 8)

#subtraction
print(12 + 8)

#division
#for floating point number
print(12 / 8)
#or for an integer
print(12 // 8)

#modulus
#returns the remainder of a division
print(12 % 8)

#exponents
print(12 ** 8)
