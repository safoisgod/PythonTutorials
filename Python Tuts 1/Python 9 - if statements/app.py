#if statements helps making conditional decisions based on the information we have

#a program for determinging what to do in what weather

is_hot = False
is_cold = True

if is_hot:
    print("It's a hot day")
    print("Drink plenty of water")
elif is_cold:
    print("It's a cold day")
    print("Wear warm cloths")
else:
    print("It's a lovely day")

print("Enjoy your day")

